function mathOperations(x, y, string) {

    if (string === '+') {
        console.log(x + y);
    } else if (string === '-') {
        console.log(x - y);
    } else if (string === '*') {
        console.log(x * y);
    } else if (string === '%') {
        console.log(x % y);
    } else if (string === '-') {
        console.log(x - y);
    } else if (string === '**') {
        console.log(x - y);
    }

}

mathOperations(5, 6, '+');
mathOperations(3, 5.5, '*');